string = input("Enter Your Name : ")
#It will print the output string
print("\nYour Name is : ",string)
#it will print captalize output
print("\nCaptalize String output : ",string.capitalize())
#It will alter the string by captalize
print("\nUpper case String : ",string.upper())
#It will alter the string by lower case
print("\nLower Case String : ",string.lower())
#It will alter the string every first letter will capital
print("\nTitle String : ",string.title())
#It will check the string is upper
print("\nIs Upper : ",string.isupper())
#It will check the string is lower
print("\nIs lower : ",string.islower())
#It will check the string is alpha numeric
print("\nIs alphanumeric : ",string.isalnum())
#It will check the string is alphabats
print("\nIs alpha : ",string.isalpha())
#It will check the string is decimal
print("\nIs decimal : ",string.isdecimal())