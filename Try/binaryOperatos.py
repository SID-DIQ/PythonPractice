num1 = int(input("Enter The First Number : "))
num2 = int(input("Enter The Second Number : "))
print("by using and operator : " ,num1&num2)
print("by using or operator : " ,num1|num2)
#it will add one to the given number and change the sign to negative
print("by using not operator : " ,~num1)
print("by using and right shift operator : " ,num1<<2)
print("by using and left shift operator : " ,num1>>2)